def add(a, b):
    """
    Return the addition of two numbers, a and b.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The result of adding a and b.
    """
    return (a + b)

# Test cases
result1 = add(5, 3)
# Expected output: 8
print("Result 1:", result1)

result2 = add(-2, 7)
# Expected output: 5
print("Result 2:", result2)

result3 = add(0, 0)
# Expected output: 0
print("Result 3:", result3)
